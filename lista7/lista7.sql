--EXERCICIO B
UPDATE PRODUCT
SET PRODUCT.VLPRICE = ( SELECT AVG(PR.VLUNITARY)
						FROM PRODUCTREQUEST PR
						WHERE PR.CDPRODUCT = PRODUCT.CDPRODUCT 
						HAVING SUM(PR.QTAMOUNT) < 800
					  );

