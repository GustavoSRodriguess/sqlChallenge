--EXERCICIO A
--N SEI SE O TOTAL SERIA O QTAMOUNT*VLUNITARY OU O VLTOTAL, VO IR PELO VLTOTAL
DELETE FROM REQUEST 
WHERE VLTOTAL < 1000
AND MONTH(DTREQUEST) = 6
AND NOT EXISTS (
    SELECT 1
    FROM PRODUCTREQUEST PR
    WHERE PR.CDREQUEST = REQUEST.CDREQUEST
);
